// métodos de array push e pop
// push = adiciona no fim do array
// pop = remove o último elemento


let nomes = ['João', 'José', 'Maria'];

let elementoRemovido = nomes.pop(); // aqui ele vai retirar o último nome do array, que é a Maria

console.log(elementoRemovido);
console.log(nomes);


nomes.push('Dani'); // aqui estamos inserindo um nome no fim do array

console.log(nomes);